import math
a = float(input("masukan nilai A: "))
b = float(input("masukan nilai B: "))

print("hasil dari perjumlahan A+B adalah:", (a+b))
print("selisih antara A dan B adalah:", (a-b))
print("jumlah A di kali B adalah:", (a*b))
print("jumlah sisa pembagian dari hasil A dibagi B adalah:", (a % b))
print("jumlah A dibagi B adalah:", (a /b))
print(f"hasil dari log(A) adalah:", math.log(a))
print("hasil A pangkat B adalah:", (a ** b))